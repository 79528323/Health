public class EmergencyContact {
    private String msg;
    private int code;
    private int data;
    private int ifExistMember;

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public int getData() {
        return data;
    }

    public void setData(int data) {
        this.data = data;
    }

    public int getIfExistMember() {
        return ifExistMember;
    }

    public void setIfExistMember(int ifExistMember) {
        this.ifExistMember = ifExistMember;
    }
}
